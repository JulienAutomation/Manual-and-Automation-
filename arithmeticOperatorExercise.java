package ProjectSample;

public class arithmeticOperatorExercise {

	public static void main(String[] args) {
	int Maths = 95;
	int Science = 91;
	int English = 92;
	int TotalMarks = (Maths+Science+English);
	
	System.out.println("Total mark = " +(TotalMarks));
	System.out.println("Average of Subjects using Float type = " +(TotalMarks/3f));
	System.out.println("Average of Subjects = " +(TotalMarks/3));
	
	}

}
